    private void bCadastraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastraActionPerformed
        Produto p = new Produto();

        double num = Double.parseDouble(tpreco.getText());
        long cod = Integer.parseInt(tcod.getText());
        p.setCodProduto(cod);
        p.setDescricao(tdesc.getText());
       
        p.setPreco(num);
        String b = (String) cbTipo.getSelectedItem();
         p.setTipo(b);

        if (tcod.getText().isEmpty() || tdesc.getText().isEmpty() || tcod.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Os campos devem ser prenenchidos !");
        } else {
            ProdutoDao dao = new ProdutoDao();
            try {
                dao.adiciona(p);
            } catch (SQLException ex) {
                Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Usuario" + tdesc.getText() + "Inserido com Sucesso !");
        }
        tcod.setText("");
        tpreco.setText("");
        tdesc.setText("");


    }//GEN-LAST:event_bCadastraActionPerformed


